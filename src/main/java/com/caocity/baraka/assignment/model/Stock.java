package com.caocity.baraka.assignment.model;

public class Stock {

	private double p;
	private String s;
	private long t;
	private short v;
	public double getP() {
		return p;
	}
	public void setP(double p) {
		this.p = p;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public long getT() {
		return t;
	}
	public void setT(long t) {
		this.t = t;
	}
	public short getV() {
		return v;
	}
	public void setV(short v) {
		this.v = v;
	}

}
